<?php

namespace App\Repository;

use App\Entity\Product;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\ORM\OptimisticLockException;
use Doctrine\ORM\ORMException;
use Doctrine\Persistence\ManagerRegistry;
use function Doctrine\ORM\QueryBuilder;

/**
 * @method Product|null find($id, $lockMode = null, $lockVersion = null)
 * @method Product|null findOneBy(array $criteria, array $orderBy = null)
 * @method Product[]    findAll()
 * @method Product[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ProductRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Product $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Product $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    public function findAllBetweenPrice($minPrice ,$maxPrice)
    {
        $query = $this->_em->createQuery("
        SELECT p  FROM App\Entity\Product as p WHERE p.price BETWEEN :minPrice AND :maxPrice
        ")->setParameters(["minPrice" => $minPrice, "maxPrice" => $maxPrice]);

        return $query->getResult();
    }

    public function findAllNameLike($name)
    {
        $query = $this->createQueryBuilder("p");
        $query= $query->where("p.name LIKE :name")->setParameter("name","{$name}%")->getQuery();
        return $query->execute();
    }


    public function findOneByIdJoinedToCategory($id) :?Product
    {
        return $this->_em->createQuery(
            "SELECT product,category FROM App\Entity\Product product
            INNER JOIN product.category category
             WHERE product.id = :id"
        )->setParameter("id",$id)->getOneOrNullResult();
    }
